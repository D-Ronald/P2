package Personagem;
public class Personagem{
    public String nome;
    public String etnia;
    public String parentesco;
    public int idade;
    public String traço;
    public Personagem(String nome,String etnia, String parentesco, int idade, String traço){
        this.nome = nome;
        this.etnia = etnia;
        this.parentesco = parentesco;
        this.idade = idade;
        this.traço = traço;
    }

}